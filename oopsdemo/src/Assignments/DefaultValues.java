package Assignments;

public class DefaultValues {

		static int Int;
		static float fl;
		static long ln;
		static double dbl;
		static short sh;
		static byte Byte;
		static boolean isEven;
		
		public static void main(String[] args) {
			System.out.println("The default values of Primitive data types are");
			
			System.out.println("int is "+Int);
			System.out.println("float is "+fl);
			System.out.println("long is "+ln);
			System.out.println("double is "+dbl);
			System.out.println("short is "+sh);
			System.out.println("byte is "+Byte);
			System.out.println("boolean is "+isEven);
		}
		
		
}
